        <h1>Why you need a framework?</h1>
        <p>
            You might have never considered using a framework for your web app. Maybe because you don't want to spend time researching and testing. But once you find the right one - it pays off.
        </p>
        <h2>Focus</h2>
        <p>
            A framework helps you focus on what's unique in your project, rather than the technology and edge cases (like requesting missing page). It also provides tools you might need (request handling, assets helpers, data storage, logging, etc.).
        </p>
        <h2>A strong foundation</h2>
        <p>
            You can expect that the framework does it's job efficiently and as expected, so no need to reinvent the wheel. You can also get updates and fixes from the maintainers.
        </p>
        <h2>A set of tools</h2>
        <p>
           	A framework is just a set of tools that help you do your job faster. And better tools can make you more efficient. We think we've created such tools, so we encourage you to try Bear Framework.
        </p>
